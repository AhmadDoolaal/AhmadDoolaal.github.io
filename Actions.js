    // Apply color choice to selected button
    function applyColorChoice(colorClass) {
        if (!selectedButton) return;
        
        // Remove all color classes
        selectedButton.classList.remove('grey', 'red', 'orange', 'winning-button');
        
        // Add selected color class
        selectedButton.classList.add(colorClass);
        
        // Update state (1 for green/red, 2 for orange)
        selectedButton.dataset.state = colorClass === 'red' ? '1' : '2';
        
        // Update counter and check win condition
        updateCounter();
        checkWin();
        
        // Close modal and reset selected button
        closeColorChoiceModal();
        selectedButton = null;
    }

    // Rotate letters (hide 3 random, show 3 hidden)
    function rotateLetters() {
        if (!gameActive) {
            // Start new game
            initializeLetters();
            createButtons();
            return;
        }
        
        // Random 3 letters that will be hiden
        const lettersToHide = [];
        const tempVisible = [...visibleLetters];
        
        while (lettersToHide.length < 3 && tempVisible.length > 0) {
            const randomIndex = Math.floor(Math.random() * tempVisible.length);
            lettersToHide.push(tempVisible.splice(randomIndex, 1)[0]);
        }
        
        // Randomly select 3 letters to show from hidden
        const lettersToShow = [];
        const tempHidden = [...hiddenLetters];
        
        while (lettersToShow.length < 3 && tempHidden.length > 0) {
            const randomIndex = Math.floor(Math.random() * tempHidden.length);
            lettersToShow.push(tempHidden.splice(randomIndex, 1)[0]);
        }
        
        // Update letter arrays
        visibleLetters = visibleLetters.filter(l => !lettersToHide.includes(l));
        visibleLetters.push(...lettersToShow);
        
        hiddenLetters = hiddenLetters.filter(l => !lettersToShow.includes(l));
        hiddenLetters.push(...lettersToHide);
        
        // Recreate buttons
        createButtons();
    }
    
