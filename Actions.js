    // Show question for selected letter
    function showQuestionForLetter(letter) {
        // Update the button text
        document.getElementById('letterQuestionBtn').textContent = letter;
        
        // Get available questions (not used yet)
        const allQuestions = letterQuestions[letter];
        const availableQuestions = allQuestions.filter(q => !usedQuestions[letter].includes(q));
        
        if (availableQuestions.length === 0) {
            // If all questions have been used, reset for this letter
            usedQuestions[letter] = [];
            showQuestionForLetter(letter); // Try again
            return;
        }
        
        // Select random question from available ones
        const randomIndex = Math.floor(Math.random() * availableQuestions.length);
        const selectedQuestion = availableQuestions[randomIndex];
        
        // Mark question as used
        usedQuestions[letter].push(selectedQuestion);
        
        // Show the question modal
        document.getElementById('questionText').textContent = selectedQuestion;
        document.getElementById('questionModal').style.display = 'flex';
    }
    
