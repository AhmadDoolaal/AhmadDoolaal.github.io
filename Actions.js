    // Create buttons
    function createButtons() {
        gameActive = true;
        document.getElementById('winMessage').style.display = 'none';
        
        const app = document.getElementById('app');
        app.innerHTML = '';
        
        // Create 6 rows
        for (let row = 0; row < 6; row++) {
            const rowDiv = document.createElement('div');
            rowDiv.className = 'button-row';
            
            // Add 5 buttons per row
            for (let i = 0; i < 5; i++) {
                const index = row * 5 + i;
                if (index >= visibleLetters.length) break;
                
                const button = document.createElement('div');
                button.className = 'hexagon-button grey';
                button.dataset.letter = visibleLetters[index];
                button.dataset.state = '0';
                button.dataset.visible = 'true';
                button.style.pointerEvents = 'auto';
                
                button.innerHTML = `
                    <div class="button-content">
                        <div class="letter">${visibleLetters[index]}</div>
                    </div>
                `;
                
                rowDiv.appendChild(button);
            }
            
            app.appendChild(rowDiv);
        }
        
        // Add click handlers
        addButtonHandlers();
        // Update counter
        updateCounter();
    }

